function updateContent() {
	document.querySelectorAll('[data-i18n]').forEach(function (element) {
		const key = element.getAttribute('data-i18n');
		element.textContent = i18next.t(key);
	});

	const isArabic = i18next.language === 'ar';
	const stylesheetLTR = document.getElementById('stylesheet-ltr');
	const stylesheetRTL = document.getElementById('stylesheet-rtl');
	stylesheetLTR.disabled = isArabic;
	stylesheetRTL.disabled = !isArabic;
}


function setLanguage(lang) {
	i18next.changeLanguage(lang, function (err, t) {
	  updateContent();
	  document.getElementById('selected-language').textContent = lang === 'ar' ? 'العربية' : 'English';
	  document.getElementById('selected-flag').src = lang === 'ar' ? '/Flag-Saudi-Arabia.png' : '/US_flag_49_stars.svg.png';
	});

	// Store the selected language in a cookie
	document.cookie = `language=${lang}; path=/; expires=Tue, 31 Dec 2030 23:59:59 GMT`;
  }

  function getLanguage() {
	const cookies = document.cookie.split(';');
	for (let i = 0; i < cookies.length; i++) {	 
	  const cookie = cookies[i].trim();
	  if (cookie.startsWith('language=')) {
		return cookie.substring('language='.length);
	  }
	}
	return null;
  }

  const storedLanguage = getLanguage();
  if (storedLanguage) {
	setLanguage(storedLanguage);
  }

  const dropdownItems = document.querySelectorAll('.dropdown-item');
  dropdownItems.forEach(item => {
	item.addEventListener('click', function (event) {
	  event.preventDefault(); // Prevent default link behavior
	  const lang = this.getAttribute('data-lang');
	  console.log(lang);
	  console.log(getLanguage());
		if (getLanguage() !== lang) {
			setLanguage(lang);
			window.location.reload();
		}
	  
	});
  });

